Transparent Development Programme (TDP) is a nongovernmental and not for profit organization which was started its developmental journey from small initiatives, TDP launched in 2015 by a group of Social, activists and development professionals after flood situation in Pakistan. The propose of this organization is to work with transparency and accountability in Education, Health, Peace and Harmony, Shelter, Water and Sanitation, Agriculture, Human Rights, Disaster Risk Reduction, Climate Change and service provision for marginalized communities.  
TDP identifies, and consequently presents innovative and cost effective solutions to fill implementation gaps, building the capacities of the involved agencies and stakeholders to effectively address the issues hampering the process. Through extensive training programs and capacity building workshops which cater to all the stakeholders involved in the process, TDP helps to ensure a lasting impact. These capacity building exercises are targeted towards Government Line departments, community based organizations, Social and Human Rights activists, Media and other stake holders. 
TDP will promote the concepts of communities, assessment on ground, researches, work through dialogs and focus gender based project in their future planning.     
 
The prelude efforts were highly challenging because, the area of work of TDP is amongst the marginalized & vulnerable areas of Sindh therefore the local youth were having ample problems to access to national & international organizations to attract them to work in the area & understand its environment & Circumstances. 
 
The determination & zeal of young professionals involved in this unceasing struggle was towering & cemented. After the elapsing of around many years of dedicated efforts, TDP is amongst the well reputed & committed organizations working for the down trodden living under pitiable circumstances at gross root level. 

